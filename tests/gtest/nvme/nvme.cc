class nvme_tx : public tcp_send_zc {
protected:
    struct mr {
        ibv_mr *reg_handle;
        void *memory;
        uint32_t size;
        mr() = default;
        mr(ibv_mr *hndl, void *mem, uint32_t sz)
            : reg_handle(hndl)
            , memory(mem)
            , size(sz) {};
        ~mr()
        {
            ibv_dereg_mr(reg_handle);
            free(memory);
        }
    };

    struct auto_close_socket {
        auto_close_socket()
            : auto_close_socket(-1) {};
        auto_close_socket(int fd)
            : m_fd(fd) {};
        ~auto_close_socket() { close(m_fd); }
        int m_fd;
    };
    vector<auto_close_socket> sockets;
    vector<mr> mrs;
    int client_fd;
    bool nvme_supported = true;
    msghdr *msg = nullptr;
    uint8_t *cmsg_buffer = nullptr;
    vector<iovec> msghdr_iov {};

    void TearDown() override
    {
        sockets.clear();
        mrs.clear();
        if (msg) {
            delete msg;
        }
        if (cmsg_buffer) {
            delete cmsg_buffer;
        }
        msghdr_iov.clear();
    }

    bool is_nvme_supported()
    {
        bool nvme_support = false;
        int pid = fork();
        if (0 != pid) {
            int cfd = tcp_base::sock_create();
            int rc = bind(cfd, (sockaddr *)&client_addr, sizeof(client_addr));
            barrier_fork(pid, true);
            rc |= connect(cfd, (sockaddr *)&server_addr, sizeof(server_addr));
            rc |= setsockopt(cfd, IPPROTO_TCP, TCP_ULP, "nvme", 4);
            nvme_support = (rc == 0);
            close(cfd);
            wait_fork(pid);
        } else { // I am the child
            int listen_fd = tcp_base::sock_create();
            int reuse_on = 1;
            int rc = setsockopt(listen_fd, SOL_SOCKET, SO_REUSEPORT, &reuse_on, sizeof(reuse_on));
            rc |= bind(listen_fd, (sockaddr *)&server_addr, sizeof(server_addr));
            rc |= listen(listen_fd, 5);
            barrier_fork(pid, true);
            int server_fd = accept(listen_fd, nullptr, nullptr);
            peer_wait(server_fd);
            close(server_fd);
            close(listen_fd);
            exit(testing::Test::HasFailure());
        }

        return nvme_support;
    }

    void client_socket_create()
    {
        client_fd = tcp_base::sock_create();
        ASSERT_GE(client_fd, 0) << "Unable to open the client socket";
        sockets.emplace_back(client_fd);

        auto rc = bind(client_fd, (sockaddr *)&client_addr, sizeof(client_addr));
        ASSERT_EQ(0, rc) << "Unable to bind to address " << sys_addr2str((sockaddr *)&client_addr);

        barrier_fork();
        rc = connect(client_fd, (sockaddr *)&server_addr, sizeof(server_addr));
        ASSERT_EQ(0, rc) << "Unable to connect to address "
                         << sys_addr2str((sockaddr *)&server_addr);

        /* ------------------NVME/TCP offset setup--------------------------- */
        rc = setsockopt(client_fd, IPPROTO_TCP, TCP_ULP, "nvme", 4);
        nvme_supported = (rc == 0);
        ASSERT_EQ(0, rc) << "NVME is unsupported";

        uint32_t configure = XLIO_NVME_HDGST_ENABLE | XLIO_NVME_DDGST_ENABLE |
            XLIO_NVME_DDGST_OFFLOAD | 0U /* pda */;
        rc = setsockopt(client_fd, NVDA_NVME, NVME_TX, &configure, sizeof(configure));
        ASSERT_EQ(0, rc) << "NVME_TX is unsupported";

        int opt_val = 1;
        rc = setsockopt(client_fd, SOL_SOCKET, SO_ZEROCOPY, &opt_val, sizeof(opt_val));
        ASSERT_EQ(0, rc);
    }

    void client_register_pdu_iov_memory(test_iovec &pdus)
    {
        /* ------------------Memory domain extraction------------------------ */
        xlio_pd_attr pd_attr;
        auto pd_attr_in_out_len = static_cast<socklen_t>(sizeof(pd_attr));
        auto rc = getsockopt(client_fd, SOL_SOCKET, SO_XLIO_PD, &pd_attr, &pd_attr_in_out_len);
        ASSERT_EQ(0, rc);
        ASSERT_EQ(sizeof(pd_attr), pd_attr_in_out_len);
        ASSERT_NE(nullptr, pd_attr.ib_pd);

        /* ------------------Memory registration----------------------------- */
        mrs.reserve(pdus.size());
        for (const auto &pdu : pdus) {
            auto buf = create_tmp_buffer(pdu.iov_len);
            ASSERT_NE(nullptr, buf) << "Valid test buffer reguired";
            memcpy(buf, pdu.iov_base, pdu.iov_len);
            auto pd = reinterpret_cast<ibv_pd *>(pd_attr.ib_pd);
            auto reg_mr = ibv_reg_mr(pd, buf, pdu.iov_len, IBV_ACCESS_LOCAL_WRITE);
            ASSERT_NE(nullptr, reg_mr) << "Memory registration failed";
            mrs.emplace_back(reg_mr, buf, pdu.iov_len);
        }
    }

    void client_prepare_msghdr_with_pdu_per_iov(size_t pdus_size)
    {
        /* ------------------Sendmsg parameter preparation------------------- */
        transform(mrs.begin(), mrs.end(), back_inserter(msghdr_iov), [](mr &m) {
            return iovec {m.memory, m.size};
        });

        size_t pd_key_len = sizeof(xlio_pd_key) * mrs.size();
        cmsg_buffer = new uint8_t[CMSG_SPACE(pd_key_len)];
        msg = new msghdr(
            {nullptr, 0, &msghdr_iov[0], pdus_size, &cmsg_buffer[0U], CMSG_LEN(pd_key_len), 0});

        cmsghdr *cmsg = CMSG_FIRSTHDR(msg);
        cmsg->cmsg_level = SOL_SOCKET;
        cmsg->cmsg_type = SCM_XLIO_NVME_PD;
        cmsg->cmsg_len = msg->msg_controllen;

        transform(mrs.begin(), mrs.end(), reinterpret_cast<xlio_pd_key *>(CMSG_DATA(cmsg)),
                  [](mr &m) {
                      return xlio_pd_key {.message_length = m.size, .mkey = m.reg_handle->lkey};
                  });
    }
    void client_post_test_stuff()
    {
        epoll_event event;
        event.events = EPOLLOUT;
        event.data.fd = client_fd;
        auto rc = test_base::event_wait(&event);
        EXPECT_GE(rc, 0);
        ASSERT_TRUE(EPOLLOUT & event.events);

        uint32_t lo, hi;
        rc = do_recv_expected_completion(client_fd, lo, hi, 1);
        EXPECT_GE(rc, 0);

        peer_wait(client_fd);
    }

    void client_process(test_iovec &pdus)
    {
        client_socket_create();
        client_register_pdu_iov_memory(pdus);
        client_prepare_msghdr_with_pdu_per_iov(pdus.size());

        if (!nvme_supported) {
            close(client_fd);
            return;
        }

        auto rc = sendmsg(client_fd, msg, MSG_ZEROCOPY);
        ASSERT_EQ(total_test_iovec_size(pdus), rc) << strerror(errno);

        client_post_test_stuff();
    }

    void server_process(int child_pid, test_iovec &pdus)
    {
        int listen_fd;
        sockaddr peer_addr;
        socklen_t socklen;

        listen_fd = tcp_base::sock_create();
        ASSERT_LE(0, listen_fd);
        sockets.emplace_back(listen_fd);
        int reuse_on = 1;
        ASSERT_EQ(0, setsockopt(listen_fd, SOL_SOCKET, SO_REUSEPORT, &reuse_on, sizeof(reuse_on)))
            << "Unable to reuse the server port";
        auto rc = bind(listen_fd, (sockaddr *)&server_addr, sizeof(server_addr));
        ASSERT_EQ(0, rc) << "Unable to bind to address " << sys_addr2str((sockaddr *)&server_addr);

        rc = listen(listen_fd, 5);
        ASSERT_EQ(0, rc) << "Unable to listen";

        barrier_fork(child_pid);

        socklen = sizeof(peer_addr);
        auto server_fd = accept(listen_fd, &peer_addr, &socklen);
        ASSERT_LE(0, server_fd);
        sockets.emplace_back(server_fd);

        for (const auto &pdu : pdus) {
            auto buf = std::make_unique<uint8_t[]>(pdu.iov_len);
            size_t data_received = 0;
            rc = 0;
            do {
                rc = recv(server_fd, (void *)&buf[data_received], pdu.iov_len - data_received, 0);
            } while ((rc >= 0 || (rc == -1 && errno == EINTR)) &&
                     (data_received += rc < 0 ? 0U : static_cast<size_t>(rc)) < pdu.iov_len);

            ASSERT_EQ(pdu.iov_len, data_received);
            ASSERT_EQ(0, memcmp(buf.get(), pdu.iov_base, pdu.iov_len));
        }

        ASSERT_EQ(0, wait_fork(child_pid));
    }
};

TEST_F(nvme_tx, send_single_pdu)
{
    SKIP_TRUE(is_nvme_supported(), "NVME offload not supported");
    SKIP_TRUE(!getenv("XLIO_TCP_CTL_THREAD"), "Skip non default XLIO_TCP_CTL_THREAD");

    int pid = fork();
    uint32_t empty_ddgst;

    test_iovec tx_iovs = {
        {.iov_base = pdu_header, .iov_len = pdu_hdr_len},
        {.iov_base = pdu_data, .iov_len = pdu_data_len},
        {.iov_base = reinterpret_cast<void *>(&empty_ddgst), .iov_len = sizeof(empty_ddgst)},
    };

    test_iovec rx_iovs = {{.iov_base = pdu_full, .iov_len = pdu_full_len}};

    if (0 == pid) { /* I am the child */
        client_process(tx_iovs);
        exit(testing::Test::HasFailure());
    } else {
        server_process(pid, rx_iovs);
    }
}

